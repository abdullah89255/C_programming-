#include <stdio.h>
int main() {
    int number = 25;
    int age = 15;
    char starr = 'a'; // Single character
    float pi = 3.50;

    printf("My number is %d \n", number);
    printf("My age is %d \n", age);
    printf("My character is %c \n", starr);
    printf("The value of pi is %f \n", pi); // Limiting to 2 decimal places

    return 0;
}